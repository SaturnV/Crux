#! /usr/bin/perl
###### NAMESPACE ##############################################################

package Crux::Controller::TransactionMixin;

###### IMPORTS ################################################################

use Essence::Strict;

###### METHODS ################################################################

# ==== Wrap ===================================================================

sub wrap_transaction
{
  my ($self, $s, @rest) = @_;
  return $self->app()->WrapInTransaction($s,
      sub
      {
        # my ($s) = @_;
        return $self->NextHandler($_[0], @rest);
      });
}

